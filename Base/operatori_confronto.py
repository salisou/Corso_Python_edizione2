
# Gli Opeatori di confronto (==, !=, <, >, <=, >=) (Vero/falso)


print("Benvebuto nel programma\n")
print("===================================================\n")
print("Inserisci 2 numeri\n")

num1 = int(input("Inserisci il primo numero:  👉 ")) 
num2 = int(input("Inserisci il secondo numero: 👉 ")) 


# Gli Opeatori di confronto (==, !=, <, >, <=, >=) (Vero/falso)

print(num1 > num2)  # Vero
print(num1 < num2)  # Falso
print(num1 != num2) # Vero
print(num1 == num2)
print(num1 <= num2)
print(num1 >= num2)

