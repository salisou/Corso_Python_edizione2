# Matematica ( +, -, *, /, % )

num1 = int(input("Inserisci il primo numero: "))
num2 = int(input("Inserisci il secondo numero: "))


# Manuel 
risultato = num1 + num2
print(f"\nLa somma di {num1} + {num2} è {risultato} {type(risultato)}\n" ) # somma 

print("====================== Sottrazione ============================\n")
# Melania
risultato = num1 - num2
print(f"la sottrazione di {num1} - {num2} è {risultato} {type(risultato)}\n")

print("====================== Moltiplicazione ============================\n")

# Filipo
risultato = num1 * num2
print(f"La moltiplicazione di {num1} per {num2} è uguale a {risultato}\n {type(risultato)}")


# Brenda
